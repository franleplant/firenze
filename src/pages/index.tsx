import type { NextPage } from "next";
import Head from "next/head";
import {
  useConnect,
  useEagerConnect,
  useWeb3React,
} from "client/modules/wallet";
import Account from "components/Account";
import Chain from "components/Chain";

import styles from "../styles/Home.module.css";
import { useNfts } from "client/io/nfts";
import Avatar from "components/Avatar";
import useSelfID from "client/modules/wallet/useSelfID";
import { useRouter } from "next/dist/client/router";

const Home: NextPage = () => {
  useEagerConnect();
  // TODO handle error
  const { account, active } = useWeb3React();
  const login = useConnect();
  const { data: nfts } = useNfts(account);

  // ceramic tests
  const { data: selfID } = useSelfID();
  const router = useRouter();

  // TODO abstract
  const avatarUrl = (() => {
    // Grab the first, but evenutally grab the chosen one
    const meta = nfts?.result?.find((element) => !!element.metadata);
    if (!meta) {
      return;
    }
    return JSON.parse(meta.metadata)?.image;
  })();

  return (
    <div className="">
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main className={styles.main}>
        {/* TODO this should be part of a "nav" or "header" */}
        {active && account ? (
          <div>
            Hello <Account /> on <Chain />
            <Avatar url={avatarUrl} />
          </div>
        ) : (
          <button onClick={login}>Login with metamask</button>
        )}

        <h1 className={styles.title}>
          Welcome to Firenze
        </h1>

        <button onClick={() => router.push("/posts")}>My Posts</button>
        <button onClick={() => router.push("/posts/creation")}>Create a Post</button>

      </main>

      <footer className={styles.footer}>
          Ceramic SelfID ID: 
          {selfID ? (
            <> {selfID?.id}</>
          ) : (
            <>Not available</>
          )}
      </footer>
    </div>
  );
};

export default Home;
